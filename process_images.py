# --- Helper function for ASpanFormer demo_utils ---
def resize(img, long_dim):
    h, w = img.shape[:2]
    scale = long_dim / max(h, w)
    h_new, w_new = int(round(h * scale)), int(round(w * scale))
    img = cv2.resize(img, (w_new, h_new), interpolation=cv2.INTER_AREA)
    return img

def draw_match(img0, img1, mkpts0, mkpts1):
    H0, W0 = img0.shape[:2]
    H1, W1 = img1.shape[:2]
    H, W = max(H0, H1), W0 + W1
    out = 255 * np.ones((H, W, 3), np.uint8)
    out[:H0, :W0] = img0
    out[:H1, W0:] = img1

    if len(img0.shape) == 2: # Grayscale
        out_rgb = cv2.cvtColor(out, cv2.COLOR_GRAY2BGR)
    else:
        out_rgb = out

    for (x0, y0), (x1, y1) in zip(mkpts0, mkpts1):
        color = tuple(np.random.randint(0, 255, 3).tolist())
        cv2.line(out_rgb, (int(x0), int(y0)), (int(x1) + W0, int(y1)), color, 1)
        cv2.circle(out_rgb, (int(x0), int(y0)), 2, color, -1)
        cv2.circle(out_rgb, (int(x1) + W0, int(y1)), 2, color, -1)

    return out_rgb
